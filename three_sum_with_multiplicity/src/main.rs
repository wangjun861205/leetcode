struct Solution;

use std::collections::HashMap;

impl Solution {
    pub fn three_sum_multi(arr: Vec<i32>, target: i32) -> i32 {
        let mut all: HashMap<i32, i32> = HashMap::new();
        arr.iter().for_each(|v| {
            *all.entry(*v).or_insert(0) += 1;
        });
        let l: Vec<HashMap<i32, i32>> = (0..arr.len())
            .map(|i| {
                *all.get_mut(&arr[i]).unwrap() -= 1;
                all.clone()
            })
            .collect();
        let mut ans = 0_i64;
        for i in 0..arr.len() - 2 {
            for j in i + 1..arr.len() - 1 {
                ans += *l[j].get(&(target - arr[i] - arr[j])).or(Some(&0)).unwrap() as i64;
            }
        }
        (ans % 1000000007) as i32
    }
}
